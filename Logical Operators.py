#LOGICAL OPERATORS

price = 25

print(price>10 and price<30) #logical and operator
#here the entire statement is true, even if one of them is false the whole output will be false

print(price>10 or price<30) #logical or operator
#here the entire statement is not true but even if one of them is true the whole output will be true

print(not price>30)
#here the statement is false but if we apply the not operator the output decision will be reversed that is false will become true
